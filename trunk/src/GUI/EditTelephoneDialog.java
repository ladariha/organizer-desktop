        kontakt.setHodnota(jTextField1.getText());
        kontakt.setOznaceni(jTextField2.getText());
        kontakt.setIdPolozky(idP);
        kontakt.setTyp("telefon");
        kontakt.setId(idPhone);
        try {
            ContactManager.savePhone(kontakt);
        } catch (Exception ex) {
            ErrorDialog ed = new ErrorDialog(new javax.swing.JFrame(), true, "Error while saving phone", ex.getMessage());
            ed.setVisible(true);
        }
        iDialog.reCreate();
