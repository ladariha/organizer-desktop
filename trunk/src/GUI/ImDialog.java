        kontakt.setHodnota(jTextField1.getText());
        kontakt.setOznaceni(jTextField2.getText());
        kontakt.setIdPolozky(idP);
        kontakt.setTyp("IM");
        kontakt.setTyp2(jTextField3.getText());
