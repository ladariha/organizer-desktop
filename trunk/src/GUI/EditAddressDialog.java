    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Adresa adresa = new Adresa();
        adresa.setUlice(jTextField1.getText());
        adresa.setCp(jTextField2.getText());
        adresa.setMesto(jTextField3.getText());
        try {
            int cislo = Integer.valueOf(jTextField4.getText()).intValue();
            adresa.setPsc(cislo);
        } catch (Exception e) {
            adresa.setPsc(0);

        }
        adresa.setId(idAdresa);
        adresa.setIdPolozky(idP);
        adresa.setTyp(jTextField5.getText());
        try {
            ContactManager.saveAddress(adresa);
        } catch (Exception ex) {
            ErrorDialog ed = new ErrorDialog(new javax.swing.JFrame(), true, "Error while saving address", ex.getMessage());
            ed.setVisible(true);
        }
        iDialog.reCreate();
        this.setVisible(false);

    }//GEN-LAST:event_jButton1ActionPerformed

